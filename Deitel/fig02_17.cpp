// Repetição controlada por contador
#include <iostream.h>
#include <conio.h>

int main()
{
  // inicialização, condição de repetição e incremento
  // estão todas incluídas no cabeçalho da estrutura for

  for (int counter=1; counter<=10; counter++)
    cout << counter << "\n";

  getch();
  return 0;
}

